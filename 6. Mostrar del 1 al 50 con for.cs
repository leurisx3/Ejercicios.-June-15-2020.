using System;

namespace Mostrar_numeros_hasta_el_50_con_for
{
    class Program
    {
        static void Main(string[] args)
        {
            //Ejercicio 6
            //Recorrer numeros del 1 al 50 con ciclo for

            for (int i = 1; i <= 50; i++)
            {
                Console.WriteLine($"Numero: {i}");
            }
        }
    }
}
